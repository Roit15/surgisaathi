<p>Laser surgery uses focused laser energy (typically a diode laser at 1470nm wavelength) to shrink, coagulate, or vaporise tissue. It's minimally invasive — no cutting, no stitches. The laser causes controlled thermal damage that leads to fibrosis (tissue shrinkage) over days to weeks.</p>

